function includeExamples(){
	let html = "";

	for(let item of examples){
		let thumb = `
			<a href="${item.link}" target='_blank'>
				<div class="thumb" style="background-image: url('${item.thumb}'); ">
					<div class="thumb-label">${item.title}</div>
				</div>
			</a>
		`;
		
		html += thumb + "\n";
	}
	
	document.write(html);
}

